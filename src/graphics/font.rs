/// 8x16フォントの幅定数
pub const FONT_WIDTH: u32 = 8;
/// 8x16フォントの高さ定数
pub const FONT_HEIGHT: u32 = 16;

// ============================================================================
// Bitmap Font
// ============================================================================

/// 8x16ビットマップフォント（基本ASCII）
pub struct BitmapFont {
    /// フォントデータ（各文字16バイト）
    data: &'static [u8],
    /// 文字幅
    width: u32,
    /// 文字高さ
    height: u32,
}

impl BitmapFont {
    /// 組み込みの8x16フォントを取得
    pub fn default_8x16() -> Self {
        Self {
            data: &DEFAULT_FONT_8X16,
            width: 8,
            height: 16,
        }
    }

    /// 文字を描画
    pub fn draw_char(
        &self,
        fb: &mut Framebuffer,
        x: i32,
        y: i32,
        c: char,
        color: Color,
        bg: Option<Color>,
    ) {
        let c = c as usize;
        if c >= 128 {
            return;
        }

        let glyph_start = c * self.height as usize;
        let glyph_end = glyph_start + self.height as usize;

        if glyph_end > self.data.len() {
            return;
        }

        for (row, &byte) in self.data[glyph_start..glyph_end].iter().enumerate() {
            for col in 0..self.width {
                let pixel_on = (byte >> (7 - col)) & 1 != 0;
                let py = y + row as i32;
                let px = x + col as i32;

                if pixel_on {
                    fb.set_pixel(px, py, color);
                } else if let Some(bg_color) = bg {
                    fb.set_pixel(px, py, bg_color);
                }
            }
        }
    }

    /// 文字列を描画
    pub fn draw_string(
        &self,
        fb: &mut Framebuffer,
        x: i32,
        y: i32,
        s: &str,
        color: Color,
        bg: Option<Color>,
    ) {
        let mut cx = x;

        for c in s.chars() {
            if c == '\n' {
                // 改行は無視（必要に応じて対応）
                continue;
            }

            self.draw_char(fb, cx, y, c, color, bg);
            cx += self.width as i32;
        }
    }

    /// 文字幅を取得
    pub fn width(&self) -> u32 {
        self.width
    }

    /// 文字高さを取得
    pub fn height(&self) -> u32 {
        self.height
    }

    /// フォントデータ長を取得
    pub fn data_len(&self) -> usize {
        self.data.len()
    }

    /// フォントデータを取得
    pub fn get_data(&self, index: usize) -> u8 {
        self.data[index]
    }
}

